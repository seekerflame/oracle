if (doImport) {
    const phrase = args.find((_, i) => args[i - 1] === '--phrase');
    if (!phrase) {
        console.log('Usage: node generate.js --import --phrase "your twelve word seed phrase here"');
        process.exit(1);
    }
    const wallet = WalletGenerator.importFromMnemonic(phrase);
    console.log(`  Imported ${wallet.chain} wallet: ${wallet.address}`);
    WalletGenerator.saveWallet(wallet, label);
    WalletGenerator.updateEnv(wallet);
    await WalletGenerator.registerWithAggregator(wallet, label);
    process.exit(0);
}

