import 'dotenv/config';
/**
 * Oracle Trading Engine — Wallet Generator
 * 
 * Generates wallets LOCALLY on your machine.
 * Private keys and seed phrases NEVER leave your computer.
 * 
 * Creates:
 * - Solana wallet (for Jupiter DEX, SOL ecosystem)
 * - Ethereum wallet (works on ETH, Arbitrum, Base, Polygon — same key)
 * 
 * Security:
 * - Keys stored in data/keys/ (gitignored, never committed)
 * - Seed phrase printed ONCE — write it down on paper
 * - .env updated with wallet addresses for Oracle to use
 * 
 * Usage:
 *   node src/wallet/generate.js                  # Generate both Solana + ETH wallets
 *   node src/wallet/generate.js --solana          # Solana only
 *   node src/wallet/generate.js --ethereum        # Ethereum only
 *   node src/wallet/generate.js --import          # Import existing seed phrase
 */

import { Keypair } from '@solana/web3.js';
import { ethers } from 'ethers';
import * as bip39 from 'bip39';
import { writeFileSync, readFileSync, existsSync, mkdirSync, appendFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const KEYS_DIR = resolve(__dirname, '../../data/keys');
const ENV_FILE = resolve(__dirname, '../../.env');

// Ensure keys directory exists and is protected
mkdirSync(KEYS_DIR, { recursive: true });

class WalletGenerator {
    /**
     * Generate a Solana wallet.
     * Returns keypair, address, and private key.
     */
    static generateSolana() {
        // Generate a new random keypair
        const keypair = Keypair.generate();
        const address = keypair.publicKey.toBase58();
        const privateKey = Buffer.from(keypair.secretKey).toString('hex');

        // Also generate a mnemonic for backup
        const mnemonic = bip39.generateMnemonic(128); // 12 words

        return {
            chain: 'solana',
            address,
            privateKey,
            mnemonic,
            keypairBytes: Array.from(keypair.secretKey),
        };
    }

    /**
     * Generate an Ethereum wallet (works on ETH, Arbitrum, Base, Polygon).
     * Uses HD wallet derivation from mnemonic.
     */
    static generateEthereum() {
        // Generate random wallet with mnemonic
        const wallet = ethers.Wallet.createRandom();

        return {
            chain: 'ethereum',
            address: wallet.address,
            privateKey: wallet.privateKey,
            mnemonic: wallet.mnemonic.phrase,
            path: wallet.mnemonic.path,
        };
    }

    /**
     * Import from existing seed phrase.
     */
    static importFromMnemonic(mnemonic, chain = 'ethereum') {
        if (chain === 'ethereum') {
            const wallet = ethers.Wallet.fromPhrase(mnemonic);
            return {
                chain: 'ethereum',
                address: wallet.address,
                privateKey: wallet.privateKey,
                mnemonic,
            };
        }
        // For Solana, we'd need to derive from the mnemonic differently
        // This is a simplified version
        throw new Error('Solana import from mnemonic requires ed25519 derivation — use Phantom to import');
    }

    /**
     * Save wallet info securely to local files.
     */
    static saveWallet(wallet, label) {
        const filename = `${wallet.chain}_${label || 'oracle'}_${Date.now()}.json`;
        const filepath = resolve(KEYS_DIR, filename);

        // Save full wallet info (KEEP THIS SECURE)
        const walletData = {
            chain: wallet.chain,
            address: wallet.address,
            label: label || 'oracle',
            createdAt: new Date().toISOString(),
            // Private key is saved here — file is gitignored
            privateKey: wallet.privateKey,
            mnemonic: wallet.mnemonic,
        };

        if (wallet.keypairBytes) {
            walletData.keypairBytes = wallet.keypairBytes;
        }

        writeFileSync(filepath, JSON.stringify(walletData, null, 2));

        return filepath;
    }

    /**
     * Update .env with wallet addresses.
     */
    static updateEnv(wallet) {
        let envContent = '';
        if (existsSync(ENV_FILE)) {
            envContent = readFileSync(ENV_FILE, 'utf8');
        }

        if (wallet.chain === 'solana') {
            if (!envContent.includes('SOLANA_WALLET_ADDRESS')) {
                appendFileSync(ENV_FILE, `\n# Solana Wallet (auto-generated by Oracle)\nSOLANA_WALLET_ADDRESS=${wallet.address}\nSOLANA_PRIVATE_KEY=${wallet.privateKey}\n`);
            }
        } else if (wallet.chain === 'ethereum') {
            if (!envContent.includes('ETH_WALLET_ADDRESS')) {
                appendFileSync(ENV_FILE, `\n# Ethereum/Arbitrum Wallet (auto-generated by Oracle)\nETH_WALLET_ADDRESS=${wallet.address}\nETH_PRIVATE_KEY=${wallet.privateKey}\n`);
            }
        }
    }

    /**
     * Register wallet with the wallet aggregator.
     */
    static async registerWithAggregator(wallet, label) {
        try {
            const { WalletAggregator } = await import('../data/wallets.js');
            const agg = new WalletAggregator();
            agg.addWallet(wallet.chain, wallet.address, label || 'oracle-generated');
        } catch (e) {
            // Aggregator not critical
        }
    }
}

// ─── CLI Entry Point ──────────────────────────────────────────

const args = process.argv.slice(2);
const doSolana = args.includes('--solana') || (!args.includes('--ethereum'));
const doEthereum = args.includes('--ethereum') || (!args.includes('--solana'));
const doImport = args.includes('--import');
const label = args.find((_, i) => args[i - 1] === '--label') || 'oracle';

console.log(`\n🔐 ORACLE WALLET GENERATOR`);
console.log('═'.repeat(60));
console.log('  Keys are stored LOCALLY. They never leave your computer.');
console.log('  WRITE DOWN YOUR SEED PHRASES ON PAPER. NOW.');
